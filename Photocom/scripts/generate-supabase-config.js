const fs = require('fs');
const path = require('path');

const SUPABASE_URL = process.env.SUPABASE_URL || process.env.NEXT_PUBLIC_SUPABASE_URL || process.env.PUBLIC_SUPABASE_URL;
const SUPABASE_ANON_KEY = process.env.SUPABASE_ANON_KEY || process.env.NEXT_PUBLIC_SUPABASE_ANON_KEY || process.env.PUBLIC_SUPABASE_ANON_KEY;

const outPath = path.join(__dirname, '..', 'supabase-config.js');

if (!SUPABASE_URL || !SUPABASE_ANON_KEY) {
  console.error('Missing SUPABASE_URL or SUPABASE_ANON_KEY environment variables.');
  // still write an empty stub so the app won't break
}

const content = `// Auto-generated by scripts/generate-supabase-config.js\n` +
`window.SUPABASE_URL = ${JSON.stringify(SUPABASE_URL || '')};\n` +
`window.SUPABASE_ANON_KEY = ${JSON.stringify(SUPABASE_ANON_KEY || '')};\n`;

fs.writeFileSync(outPath, content, 'utf8');
console.log('Wrote', outPath);
